                    <h2 class="blog-title"><a href="blog_single_post.html" >Considerations for Your Next Move <br/><small>(How It Can Affect Your Career)</small></a></h2>
                    <div class="blog-meta">February 27, 2015 by <a href="#">William Frierson</a></div>
                    <div class="blog-img">
                      <img src="./assets/theme/images/home-bg.jpg" alt="" class="img-full ">
                      <small >University law school graduate on graduation day. Photo courtesy of Pixabay.</small>
